first_num = int(input('first number: '))
second_num = int(input('second number: '))
operator = input('What do you want to do?: ')

if operator == '+':
    z = first_num + second_num
    print(z)

if operator == '-':
    z = first_num - second_num
    print(z)

if operator == '*':
    z = first_num * second_num
    print(z)

if operator == '/':
    z = first_num / second_num
    print(z)

